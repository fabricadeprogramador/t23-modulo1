function somar() {
    var n1 = document.getElementById("input1").value;

    var n2 = document.getElementById("input2").value;

    var resultado = parseFloat(n1) + parseFloat(n2);

    alert(resultado);
}

function subtrair() {
    var n1 = document.getElementById("input1").value;

    var n2 = document.getElementById("input2").value;

    var resultado = parseFloat(n1) - parseFloat(n2);

    alert(resultado);
}

function multiplicar() {
    var n1 = document.getElementById("input1").value;

    var n2 = document.getElementById("input2").value;

    var resultado = parseFloat(n1) * parseFloat(n2);

    alert(resultado);
}

function dividir() {
    var n1 = document.getElementById("input1").value;

    var n2 = document.getElementById("input2").value;

    var resultado = parseFloat(n1) / parseFloat(n2);

    alert(resultado);

}